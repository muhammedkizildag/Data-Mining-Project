    print(f"  LEGACY SCRIPT UYARISI — {script_name}")
    print("=" * 78)
    print(f"  Güncel script : {current_script}")
    print(f"  Not           : {reason}")
    print("  Bu dosya yalnızca geçmiş karşılaştırma / tarihsel kayıt için tutulur.")
    print("=" * 78)
